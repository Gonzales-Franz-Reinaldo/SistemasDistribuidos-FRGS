    private void VerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerificarActionPerformed
        try {
            String ci = CI.getText();
            String anio = anioField.getText();
            String impuesto = impuestoField.getText();
            String monto = montoField.getText();

            if (ci.isEmpty() || anio.isEmpty() || impuesto.isEmpty() || monto.isEmpty()) {
                Realizado.setText("Por favor, complete todos los campos.");
                return;
            }
            System.out.println(ci);
            toServer.println("Pagar:" + ci + "," + anio + "," + impuesto + "," + monto + ",");
            String pago = fromServer.readLine();
            System.out.println(pago);
            if (pago.equals("true")) {
                Realizado.setText("No se pudo realizar el pago.");
            } else {
                Realizado.setText("Se pudo realizar el pago.");
            }

        } catch (IOException ex) {
            Realizado.setText("Error al procesar el pago.");
            System.out.println("Error al leer del servidor: " + ex.getMessage());
        }
    }//GEN-LAST:event_VerificarActionPerformed


