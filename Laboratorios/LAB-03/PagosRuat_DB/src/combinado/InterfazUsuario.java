    private void DeudasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeudasActionPerformed
        try {
            String ci = CI.getText();
            if (ci.isEmpty()) {
                CiDeudas.setText("Por favor, ingrese su CI.");
                return;
            }
            toServer.println("Deuda:" + ci);
            String deudas = fromServer.readLine();
            if (deudas != null) {
                String[] partes = deudas.split(":");
                String deudasSinTermino = partes[1];
                String[] deudasArray = deudasSinTermino.split(";");
                StringBuilder resultado = new StringBuilder("<html>Las deudas con el CI: " + ci + " son:");
                
                for (String deudasI : deudasArray) {
                    String[] detalleDeuda = deudasI.split(",");
                    resultado.append("Año: ").append(detalleDeuda[0]).append("***");
                    resultado.append("Impuesto: ").append(detalleDeuda[1]).append("***");
                    resultado.append("Monto: ").append(detalleDeuda[2]).append("<br>");
                }
                resultado.append("</html>");
                CiDeudas.setText(resultado.toString());
            }
        } catch (IOException ex) {
            System.out.println("Error al leer del servidor: " + ex.getMessage());
        }
    }//GEN-LAST:event_DeudasActionPerformed

